package javastring;

public class SystemApp {
    public static void main(String[] args) {

        System.out.println();
        System.out.println("\t" + System.currentTimeMillis());
        System.out.println();
        System.out.println("\t" + System.nanoTime());
        System.out.println();
        System.out.println("\t" + System.getenv("HOME"));
        System.out.println();

        // exit method used for stopping program from running
        System.exit(0);

        System.out.println("Gilang Riyadi Kuntiarso");

    }
}
